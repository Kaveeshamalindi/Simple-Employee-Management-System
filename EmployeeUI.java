    private boolean checkVal(){
        
        if(txtEmployeeID.getText().equals("")){
            JOptionPane.showMessageDialog(controlPanel, "Employee ID cannot be blank");
            return false;
        }
        
        if(txtFirstName.getText().equals("")){
            JOptionPane.showMessageDialog(controlPanel, "First Name cannot be blank");
            return false;
        }
        
        if(txtLastName.getText().equals("")){
            JOptionPane.showMessageDialog(controlPanel, "Last Name cannot be blank");
            return false;
        }
        
        if(txtBirthDate.getText().equals("")){
            JOptionPane.showMessageDialog(controlPanel, "Birth Date cannot be blank");
            return false;
        }
        return true;
    }

