    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        if (checkVal()==true){
            
            int empID = Integer.valueOf(txtEmployeeID.getText());
            String firstName = txtFirstName.getText();
            String lastName = txtLastName.getText();
            
            String gender ="";
            if(rdoMale.isSelected()){
                gender="Male";
            } else if (rdoFemale.isSelected()){
                gender="Female";
            } else {
                gender="";
            }
            
            Date birthDate = Date.valueOf(txtBirthDate.getText());
            String qualification = cmbQualification.getSelectedItem().toString();
            emp = new Employee(empID, firstName, lastName, gender, birthDate, qualification);
            JOptionPane.showMessageDialog(controlPanel, "The employee record is added");  
        }    
    }//GEN-LAST:event_btnAddActionPerformed

